export const DEFAULT_THEME = 'almanac';

const cache = new Map();

export function getThemeExtensions(id) {
    const themeId = PALETTES[id] ? id : DEFAULT_THEME;
    if (cache.has(themeId)) return cache.get(themeId);
    const ext = buildExtensions(PALETTES[themeId]);
    cache.set(themeId, ext);
    return ext;
}
