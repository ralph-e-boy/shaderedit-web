const FONT = "'JetBrains Mono', ui-monospace, monospace";

function withPanelOpacity(color) {
    if (!color || color === 'transparent') return color;
    return `color-mix(in oklab, ${color} calc(var(--editor-panel-opacity, 0.95) * 100%), transparent)`;
}

